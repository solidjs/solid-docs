function relativePath(root, filePath) {
	return path.relative(root, filePath).split(path.sep).join("/");
}

function slugify(value) {
	return value
		.replace(/^\$/, "dollar-")
		.replace(/([a-z0-9])([A-Z])/g, "$1-$2")
		.replace(/[^a-zA-Z0-9]+/g, "-")
		.replace(/^-+|-+$/g, "")
		.toLowerCase();
}

function yamlString(value) {
	return JSON.stringify(value);
}
